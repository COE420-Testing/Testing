
public class CalculationsSoln {

	public static void main(String[] args) {
		int x = 2;
		int y = 1;
		
		//sum of x and y 
		int sum = x+y;
		System.out.println("The sum is= "+ sum);
		
		//member_B subtract x from y and print the result
		int subtraction = x-y;
		System.out.println("The difference is ="+ subtraction);

		//member_C divide x by y and print the result
		int division = x/y;
		System.out.println("The quotient is= "+ division);
		
		//member_D multiply x by y and print the result
		int multiplication = x*y;
		System.out.println("The product is = "+ multiplication);
		

	}
	

}
